    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblBankAcct.getModel();
        model.setRowCount(0);
        
        for (BankAcct ba: bankAcctHistory.getBankAcctHistory()){
            
            Object[] row = new Object[5];
            row[0] = ba; //create override method in BankAcct.java
            row[1] = ba.getAcctNumber();
            row[2] = ba.getBankName();
            row[3] = ba.getAcctType();
            row[4] = ba.getAcctBalance();
            
            model.addRow(row);
        }
    }
