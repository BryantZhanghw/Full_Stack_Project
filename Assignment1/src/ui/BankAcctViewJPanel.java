    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblBankAcct.getSelectedRow();
        
        if (selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblBankAcct.getModel();
        BankAcct seletedBankAcct = (BankAcct) model.getValueAt(selectedRowIndex, 0);
        
        txtName.setText(seletedBankAcct.getName());
        txtAcctNumber.setText(seletedBankAcct.getAcctNumber());
        txtBankName.setText(seletedBankAcct.getBankName());
        txtAcctType.setText(seletedBankAcct.getAcctType());
        txtAcctBalance.setText(seletedBankAcct.getAcctBalance());
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblBankAcct.getSelectedRow();
        
        if (selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblBankAcct.getModel();
        BankAcct seletedBankAcct = (BankAcct) model.getValueAt(selectedRowIndex, 0);
        
        bankAcctHistory.deleteBankAcct(seletedBankAcct);
        
        JOptionPane.showMessageDialog(this, "Bank account deleted.");
         
        populateTable();
        
    }//GEN-LAST:event_btnDeleteActionPerformed

