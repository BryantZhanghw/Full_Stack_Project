    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblLocalAdd.getModel();
        model.setRowCount(0);
        
        for (LocalAdd la: localAddHistory.getLocalAddHistory()){
     
        Object[] row = new Object[5];
        row[0] = la;
        row[1] = la.getStreetAdd();
        row[2] = la.getCity();
        row[3] = la.getState();
        row[4] = la.getZipCode();
        
        model.addRow(row);
               
        }
        
    }


