    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblLocalAdd.getSelectedRow();
        
        if (selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblLocalAdd.getModel();
        LocalAdd selectedLocalAdd = (LocalAdd) model.getValueAt(selectedRowIndex, 0);
        
        txtName.setText(selectedLocalAdd.getName());
        txtStreetAdd.setText(selectedLocalAdd.getStreetAdd());
        txtCity.setText(selectedLocalAdd.getCity());
        txtState.setText(selectedLocalAdd.getState());
        txtZipCode.setText(selectedLocalAdd.getZipCode());
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblLocalAdd.getSelectedRow();
        
        if (selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblLocalAdd.getModel();
        LocalAdd selectedLocalAdd = (LocalAdd) model.getValueAt(selectedRowIndex, 0);
        
        localAddHistory.deleteLocalAdd(selectedLocalAdd);
        
        JOptionPane.showMessageDialog(this, "Local address deleted.");
        
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

