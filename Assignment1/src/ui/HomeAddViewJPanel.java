    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int seletedRowIndex = tblHomeAdd.getSelectedRow();
        
        if (seletedRowIndex < 0){
            
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblHomeAdd.getModel();
        HomeAdd selectedHomeAdd = (HomeAdd) model.getValueAt(seletedRowIndex, 0);
        
        txtName.setText(selectedHomeAdd.getName());
        txtStreetAdd.setText(selectedHomeAdd.getStreetAdd());
        txtCity.setText(selectedHomeAdd.getCity());
        txtState.setText(selectedHomeAdd.getState());
        txtZipCode.setText(selectedHomeAdd.getZipCode());
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int seletedRowIndex = tblHomeAdd.getSelectedRow();
        
        if (seletedRowIndex < 0){
            
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblHomeAdd.getModel();
        HomeAdd selectedHomeAdd = (HomeAdd) model.getValueAt(seletedRowIndex, 0);
        
        homeAddHistory.deleteHomeAdd(selectedHomeAdd);
        
        JOptionPane.showMessageDialog(this, "Home address deleted.");
        
        populateTable();
        
    }//GEN-LAST:event_btnDeleteActionPerformed


