    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblHomeAdd.getModel();
        model.setRowCount(0);
        
        for (HomeAdd ha: homeAddHistory.getHomeAddHistory()){
            
            Object[] row = new Object[5];
            row[0] = ha;
            row[1] = ha.getStreetAdd();
            row[2] = ha.getCity();
            row[3] = ha.getState();
            row[4] = ha.getZipCode();
            
            model.addRow(row);
        }
    }
